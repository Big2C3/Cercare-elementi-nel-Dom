// Uso getElementsByClassName x cercare tutti gli elementi con classe paragrafo
const paragrafi = document.getElementsByClassName("paragrafo");
console.log(paragrafi);

// Uso getElementById x cercare l'elemento con id "elemento-3"
const elemento3 = document.getElementById("elemento-3");
console.log(elemento3);

// Uso querySelectorAll x cercare tutti gli elementi h2 della pagina
const titoliH2 = document.querySelectorAll("h2");
console.log(titoliH2);

